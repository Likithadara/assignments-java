
public class Lab4Loop {
    public static void main(String args[]) {
        System.out.println("N" + "    " + "10*N" + "    " + "100*N" + "     " + "1000*N");
        for (int N = 1; N <= 5; N++) {
            System.out.println(N + "    " + 10 * N + "      " + 100 * N + "        " + 1000 * N);
        }
    }
}